package lsg.bags;

/**
 * Classe MediumBag
 * Cette classe permet de définir les sacs de taille moyenne (40) qui peuvent être utilisés par le joueur
 * @see lsg.bags.Bag
 */
public class MediumBag extends Bag
{
    /**
     * Constructeur de la classe MediumBag
     * Appelle le constructeur de la classe mère (Bag) avec une capacité de 40
     */
    public MediumBag() { super(40); }
}
